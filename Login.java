    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String email = jTextField1.getText().trim();
        String password = new String(jPasswordField1.getPassword());
        
        // Validate input
        if (email.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Check user type selection
        if (!jCheckBox1.isSelected() && !jCheckBox2.isSelected()) {
            JOptionPane.showMessageDialog(this, "Please select Customer or Driver!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String userType = jCheckBox1.isSelected() ? "customer" : "driver";
        
        // Database connection
        String url = "jdbc:mysql://localhost:3306/tapnpay5";
        String dbUser = "root";
        String dbPassword = "";
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection(url, dbUser, dbPassword);
            
            // Check if user exists with matching email and password
            String query = "SELECT * FROM CUSTOMER_INFO WHERE Email = ? AND Password = ?";
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1, email);
            pst.setString(2, password);
            
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
                // Login successful
                String fullName = rs.getString("FULL_NAME");
                JOptionPane.showMessageDialog(this, "Welcome " + fullName + "!", "Login Successful", JOptionPane.INFORMATION_MESSAGE);
                
                // Navigate to appropriate home page based on user type
                if (userType.equals("customer")) {
                    Home homeFrame = new Home(email, userType);
                    homeFrame.setVisible(true);
                    homeFrame.setLocationRelativeTo(null);
                } else {
                    DriverHome driverHomeFrame = new DriverHome(email);
                    driverHomeFrame.setVisible(true);
                    driverHomeFrame.setLocationRelativeTo(null);
                }
                this.dispose();
            } else {
                // Login failed
                JOptionPane.showMessageDialog(this, "Account not Exist INVALID!", "Login Failed", JOptionPane.ERROR_MESSAGE);
                // Clear password field
                jPasswordField1.setText("");
            }
            
            rs.close();
            pst.close();
            conn.close();
            
        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this, "MySQL JDBC Driver not found!", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

